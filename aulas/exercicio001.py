primeiro_valor = float(input('Digite o primeiro valor: '))
segundo_valor = float(input('Digite o segundo valor: '))


if (primeiro_valor > segundo_valor):
    print(f'O primeiro valor é maior que o segundo!!!')
elif (primeiro_valor == segundo_valor):
    print(f'Os valores digitados possuem o mesmo valor!!!')
else:
    print(f'O primeiro valor é menor que o segundo!!!!')
